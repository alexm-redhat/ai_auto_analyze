@dataclass
class NarrowResolutionAgentPrompt:
    context: str
    fix_diff: str
    conflicted_files: list[str]
    allowed_modules: list[str]

    prompt_template: ClassVar[str] = """\
{context}

You are the Narrow Resolution Agent (Phase 3a). Cherry-pick produced conflicts in these files:
{conflicted_files}

The upstream fix diff for reference:
<fix_diff>
{fix_diff}
</fix_diff>

Your task is to resolve ONLY the conflict hunks (marked with <<<<<<<, =======, >>>>>>>). Constraints:

1. Resolve each conflict hunk to produce the intended fix behavior on the target branch.
2. Do NOT re-derive intent — the fix diff tells you exactly what should change.
3. Do NOT rewrite surrounding code or modify files outside the cherry-pick's scope.

## Priority files (check these first):
{allowed_modules}

You may modify any file needed to make the build and tests pass. If you need to add \
missing types, feature gates, or adapt API patterns for the target branch, do so.

After resolving all conflicts:
- Run `git diff --name-only HEAD` and verify every changed file is in the allowlist.
- If any file is outside the allowlist, undo that change and re-resolve.
- Stage and commit with the cherry-pick message plus structured trailers.
"""

    def prompt(self) -> str:
        return self.prompt_template.format(
            context=self.context,
            fix_diff=self.fix_diff,
            conflicted_files="\n".join(f"  - {f}" for f in self.conflicted_files),
            allowed_modules="\n".join(f"  - {f}" for f in self.allowed_modules),
        )


@dataclass
class BuildErrorRecoveryPrompt:
    """Prompt for Phase 4a — agent-assisted build/test error recovery."""
    context: str
    fix_diff: str
    current_diff: str
    build_errors: str
    test_errors: str
    allowed_modules: list[str]
    allowed_seed: list[str]
    target_branch: str
    build_command: str
    test_command: str
    iteration: int
    max_retries: int

    prompt_template: ClassVar[str] = """\
{context}

You are the Build Error Recovery Agent (Phase 4a, iteration {iteration}/{max_retries}).

The fix was cherry-picked from the source branch and conflicts were resolved, but the \
result does not compile or pass tests on the target branch.

The upstream fix for reference:
<fix_diff>
{fix_diff}
</fix_diff>

What was actually applied to the target branch:
<current_diff>
{current_diff}
</current_diff>

Build errors:
<build_errors>
{build_errors}
</build_errors>

Test errors:
<test_errors>
{test_errors}
</test_errors>

## What you CAN do:
- Add missing imports that exist on the target branch
- Fix function call signatures to match the target branch API (verify with `git grep`)
- Replace types/methods that don't exist on the target with their target-branch equivalents
- Remove references to symbols that don't exist and aren't required for the fix's intent
- Adapt API patterns (e.g. `tCtx.WithStep()` → `ktesting.WithStep(tCtx, ...)`)
- Add new type definitions locally if they are needed and don't exist on the target branch
- Modify any file needed to make the build and tests pass

## What you MUST NOT do:
- Change the semantic intent of the fix
- Stub out or mock missing functionality with no-op implementations

## Priority files (check these first):
{allowed_modules}

## Original seed files:
{allowed_seed}

## Process:
1. Read each error message carefully
2. For each undefined symbol: run `git grep <symbol> {target_branch}` to check if it exists
3. If it exists under a different name or signature → adapt the code
4. If it doesn't exist and is a core dependency of the fix → stop and report CANNOT_FIX
5. After fixing, run: {build_command}
6. Then run: {test_command}

If the errors require types, interfaces, or features that don't exist on the target branch \
and cannot be adapted without changing the fix's intent, write to stdout:
  CANNOT_FIX: <explanation of what's missing and why>
Do NOT attempt partial workarounds — either the fix compiles cleanly or it doesn't.
"""

    def prompt(self) -> str:
        """Render the Phase 4a recovery prompt with all context sections."""
        return self.prompt_template.format(
            context=self.context,
            fix_diff=self.fix_diff,
            current_diff=self.current_diff,
            build_errors=self.build_errors,
            test_errors=self.test_errors,
            allowed_modules="\n".join(f"  - {f}" for f in self.allowed_modules),
            allowed_seed="\n".join(f"  - {f}" for f in self.allowed_seed),
            target_branch=self.target_branch,
            build_command=self.build_command,
            test_command=self.test_command,
            iteration=self.iteration,
            max_retries=self.max_retries,
        )


